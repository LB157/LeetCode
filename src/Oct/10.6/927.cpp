using namespace std;

class Solution {
 public:
  vector<int> threeEqualParts(vector<int>& arr) {
    vector<int> result{-1, -1};
    vector<int> temp;
    int idx = 0;
    for (auto& a : arr) {
      if (a) temp.push_back(idx);
      ++idx;
    }
    if (temp.empty()) {
      result[0] = 0;
      result[1] = static_cast<int>(arr.size()) - 1;
      return result;
    }
    if (temp.size() % 3 != 0) return result;
    // 可以三等分
    int len = temp.size() / 3;
    int i = len - 1;
    int j = 2 * len;
    for (int k = 0; k < len - 1; k++) {
      int delta_i = temp[k + 1] - temp[k];
      int delta_j = temp[i + k + 2] - temp[i + k + 1];
      int delta_k = temp[j + k + 1] - temp[j + k];
      if (not(delta_i == delta_j and delta_j == delta_k)) return result;
    }
    // i, i + len + m
    // TODO m个尾0分配（取决于最后一个分组）
    int m = arr.size() - 1 - temp.back();
    if (temp[len - 1] + m < temp[len] and
        temp[2 * len - 1] + m < temp[2 * len]) {
      result[0] = temp[len - 1] + m;
      result[1] = temp[2 * len - 1] + m + 1;
    }
    return result;
  }
};

